/**
 * Cargo company interface with get and set methods of classes that implements the CargoCompanyInterface.
 * Get and set name of id of classes.
 * @author Medine ASLAN
 */
public interface CargoCompanyInterface
{
    /**
     * Getter method of classes name or id.
     * @return nameOrId;
     */
    String getNameOrId();

    /**
     * Setter method of classes name or id.
     * @param newNameOrId keeps the new name or id.
     */
    void setNameOrId(String newNameOrId);
}
